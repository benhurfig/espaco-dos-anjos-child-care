/* ==========================================================
   LANGUAGE SETTINGS
========================================================== */

const LANGUAGE_SETTINGS = {

    supportedLanguages: [
        "en",
        "pt",
        "es"
    ],

    defaultLanguage: "en",

    storageKey: "espacoDosAnjosLanguage"

};


/* ==========================================================
   GET INITIAL LANGUAGE
========================================================== */

function getInitialLanguage() {

    const savedLanguage =
        localStorage.getItem(
            LANGUAGE_SETTINGS.storageKey
        );

    if (
        savedLanguage &&
        LANGUAGE_SETTINGS.supportedLanguages.includes(
            savedLanguage
        )
    ) {

        return savedLanguage;

    }


    const browserLanguage =
        navigator.language
            .toLowerCase()
            .split("-")[0];

    if (
        LANGUAGE_SETTINGS.supportedLanguages.includes(
            browserLanguage
        )
    ) {

        return browserLanguage;

    }


    return LANGUAGE_SETTINGS.defaultLanguage;

}


/* ==========================================================
   TRANSLATE WEBSITE
========================================================== */

function translateWebsite(language) {

    const translations =
        WEBSITE_TRANSLATIONS[language];

    if (!translations) {

        console.warn(
            `Translation not found for language: ${language}`
        );

        return;

    }


    /* NORMAL TEXT */

    document
        .querySelectorAll("[data-i18n]")
        .forEach((element) => {

            const translationKey =
                element.dataset.i18n;

            const translatedText =
                translations[translationKey];

            if (translatedText !== undefined) {

                element.textContent =
                    translatedText;

            }

        });


    /* ARIA LABELS */

    document
        .querySelectorAll(
            "[data-i18n-aria-label]"
        )
        .forEach((element) => {

            const translationKey =
                element.dataset.i18nAriaLabel;

            const translatedText =
                translations[translationKey];

            if (translatedText !== undefined) {

                element.setAttribute(
                    "aria-label",
                    translatedText
                );

            }

        });


    /* PLACEHOLDERS */

    document
        .querySelectorAll(
            "[data-i18n-placeholder]"
        )
        .forEach((element) => {

            const translationKey =
                element.dataset.i18nPlaceholder;

            const translatedText =
                translations[translationKey];

            if (translatedText !== undefined) {

                element.setAttribute(
                    "placeholder",
                    translatedText
                );

            }

        });


    /* UPDATE HTML LANGUAGE */

    document.documentElement.lang =
        language;


    /* SAVE LANGUAGE */

    localStorage.setItem(
        LANGUAGE_SETTINGS.storageKey,
        language
    );


    /* UPDATE LANGUAGE BUTTON */

    const currentLanguage =
        document.querySelector(
            ".language-selector__current"
        );

    if (currentLanguage) {

        currentLanguage.textContent =
            language.toUpperCase();

    }


    /* UPDATE ACTIVE OPTION */

    document
        .querySelectorAll(
            ".language-selector__option"
        )
        .forEach((option) => {

            option.classList.toggle(
                "is-active",
                option.dataset.language === language
            );

        });


    /* NOTIFY OTHER FILES */

    document.dispatchEvent(
        new CustomEvent(
            "websiteLanguageChanged",
            {
                detail: {
                    language
                }
            }
        )
    );

}


/* ==========================================================
   INITIALIZE
========================================================== */

document.addEventListener(
    "DOMContentLoaded",
    () => {

        const initialLanguage =
            getInitialLanguage();

        translateWebsite(
            initialLanguage
        );


        document
            .querySelectorAll(
                ".language-selector__option"
            )
            .forEach((option) => {

                option.addEventListener(
                    "click",
                    () => {

                        const selectedLanguage =
                            option.dataset.language;

                        if (
                            LANGUAGE_SETTINGS
                                .supportedLanguages
                                .includes(
                                    selectedLanguage
                                )
                        ) {

                            translateWebsite(
                                selectedLanguage
                            );

                        }

                    }
                );

            });

    }
);


/* ==========================================================
   GLOBAL ACCESS
========================================================== */

window.WEBSITE_TRANSLATIONS =
    WEBSITE_TRANSLATIONS;

window.changeWebsiteLanguage =
    translateWebsite;